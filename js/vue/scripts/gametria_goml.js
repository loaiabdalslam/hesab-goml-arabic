 function normalize_text(text) {

    var withoutDiacs = text.replace(/([^\u0621-\u063A\u0641-\u064A\u0660-\u0669a-zA-Z 0-9])/g, '');
    return withoutDiacs
  }

function calculateGoml(dictionary_of_goml,text){
    str = normalize_text(text)
    str = str.replace(/\s/g, '');
    total_goml = 0
    for (var i = 0; i < str.length; i++) {
        try{
            total_goml += dictionary_of_goml[str[i]]
        }catch{
        }
    }
    return total_goml

}




function CharsInText(text){
    str = normalize_text(text)
    str = str.replace(/\s/g, '');
    length = str.length
    unquie = String.prototype.concat(...new Set(str))
    if(unquie){
        unique_length = unquie.length
    }
    return [Number(length),Number(unique_length)]

}

